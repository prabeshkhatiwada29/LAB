import java.util.HashSet;
public class HashSetExample {
    public static void main(String[] args) {
        HashSet<String> cources = new HashSet<String>();
        cources.add("BCA");
        cources.add("BBA");
        cources.add("BSCCSIT");
        cources.add("BE");
        System.out.println("Cources in the HashSet: "+cources);

    }
}
